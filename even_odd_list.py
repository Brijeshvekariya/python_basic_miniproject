
n = int(input("How many Number you want to add : "))
l1 = []
l2 = []
for i in range(1,n+1):
    num = int(input("Enter %d number : "%i))
    if num%2==0:
        l1.append(num)
    else:
        l2.append(num)
print("The Even List is : ",l1)
print("The Odd List is : ",l2)
